  attemptsDisplay.textContent = ''; //–æ—á–∏—â—É—î —Ç–µ–∫—Å—Ç —ñ–∑ –∫—ñ–ª—å–∫—ñ—Å—Ç—é —Å–ø—Ä–æ–±
  guessInput.value = ''; // –æ—á–∏—â—É—î –ø–æ–ª–µ –≤–≤–µ–¥–µ–Ω–Ω—è –¥–ª—è –Ω–æ–≤–æ–≥–æ —Å—Ç–∞—Ä—Ç—É –≥—Ä–∏

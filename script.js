let message = document.getElementById('message'); //  –µ–ª–µ–º–µ–Ω—Ç, –¥–µ –±—É–¥—É—Ç—å –≤—ñ–¥–æ–±—Ä–∞–∂–∞—Ç–∏—Å—è –ø—ñ–¥–∫–∞–∑–∫–∏

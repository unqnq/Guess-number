// –ì–µ–Ω–µ—Ä—É—î–º–æ –≤–∏–ø–∞–¥–∫–æ–≤–µ —á–∏—Å–ª–æ –≤—ñ–¥ 1 –¥–æ 100
//Math.floor - –æ–∫—Ä—É–≥–ª—é—î —á–∏—Å–ª–æ –¥–æ –º–µ–Ω—à–æ–≥–æ —Ü—ñ–ª–æ–≥–æ, –Ω–∞–ø—Ä–∏–∫–ª–∞–¥ 3.79 = 3
let randomNumber = Math.floor(Math.random() * 100) + 1;

// –õ—ñ—á–∏–ª—å–Ω–∏–∫ —Å–ø—Ä–æ–±
let attempts = 0;

// –û—Ç—Ä–∏–º—É—î–º–æ –∫–æ–∂–µ–Ω HTML –µ–ª–µ–º–µ–Ω—Ç –∑–∞ –π–æ–≥–æ id
let guessInput = document.getElementById('guessInput'); //—Ç–µ–∫—Å—Ç–æ–≤–µ –ø–æ–ª–µ –¥–ª—è –≤–≤–µ–¥–µ–Ω–Ω—è —á–∏—Å–ª–∞
let submitGuess = document.getElementById('submitGuess'); //–∫–Ω–æ–ø–∫–∞ "–ü–µ—Ä–µ–≤—ñ—Ä–∏—Ç–∏"
